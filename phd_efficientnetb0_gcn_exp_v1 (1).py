    ax[i + 1].imshow(Image.open(test_paths[n]))
    ax[i + 1].set_title(
        f"{class_names[test_labels[n]]}\n(sim={sim[query_index, n]:.3f})",
        fontsize=9
    )
    ax[i + 1].axis("off")

