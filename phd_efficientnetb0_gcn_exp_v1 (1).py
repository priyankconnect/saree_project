# GCN-based image retrieval: query image -> top 5 similar images
import numpy as np
import matplotlib.pyplot as plt
from PIL import Image
from sklearn.metrics.pairwise import cosine_similarity

# ---- Required inputs from your GCN pipeline ----
# node_repr        : [N, D] GCN embeddings (e.g., penultimate layer output)
# all_paths        : [N] image paths (train+val+test merged in same order as node_repr)
# true_all         : [N] integer labels for all nodes
# class_names      : list of class names in label order
# data.test_mask   : boolean mask for test nodes (torch tensor)

# Convert test mask to numpy
test_mask_np = data.test_mask.cpu().numpy()
test_indices = np.where(test_mask_np)[0]

# Use only test nodes for retrieval (recommended)
test_embeddings = node_repr[test_indices]
test_paths = all_paths[test_indices]
test_labels = true_all[test_indices]

# Build cosine similarity matrix on test embeddings
sim = cosine_similarity(test_embeddings)

# Pick query index within test set
query_index = 50   # change as needed (0 to len(test_embeddings)-1)

# Rank neighbors
nearest = np.argsort(-sim[query_index])   # descending similarity
nearest = nearest[nearest != query_index] # remove self
nearest = nearest[:5]                     # top 5

# Plot query + neighbors
fig, ax = plt.subplots(1, 6, figsize=(18, 3))

# Query image
ax[0].imshow(Image.open(test_paths[query_index]))
ax[0].set_title(f"Query\n{class_names[test_labels[query_index]]}")
