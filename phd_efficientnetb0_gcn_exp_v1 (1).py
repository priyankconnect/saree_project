def extract_embeddings(split_dir: str, model, transform, device):
    classes = sorted([d for d in os.listdir(split_dir) if os.path.isdir(os.path.join(split_dir, d))])
    class_to_idx = {name: i for i, name in enumerate(classes)}

    embeddings, labels, paths = [], [], []

    for cls in classes:
        cls_dir = os.path.join(split_dir, cls)
        files = sorted(os.listdir(cls_dir))
        for fname in tqdm(files, desc=f"Extracting {os.path.basename(split_dir)}:{cls}"):
            if not fname.lower().endswith((".jpg", ".jpeg", ".png")):
                continue

            img_path = os.path.join(cls_dir, fname)
            try:
                img = Image.open(img_path).convert("RGB")
                x = transform(img).unsqueeze(0).to(device)
                with torch.no_grad():
                    feat = model(x)
                embeddings.append(feat.squeeze().cpu().numpy())
                labels.append(class_to_idx[cls])
                paths.append(img_path)
            except Exception as e:
                print(f"Skipping {img_path}: {e}")

    return np.array(embeddings), np.array(labels), np.array(paths), classes


def save_split_embeddings(save_root: str, split: str, embeddings, labels, paths):
    os.makedirs(save_root, exist_ok=True)
